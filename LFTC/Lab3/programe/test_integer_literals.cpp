// Test program cu toate tipurile de constante intregi din C++
#include <iostream>
int main() {
    // Literali decimali
    int dec1 = 42;
    int dec2 = 999;
    int dec3 = 12345;
    
    // Literali octali
    int oct1 = 0;
    int oct2 = 077;
    int oct3 = 0123;
    
    // Literali hexazecimali (lowercase x)
    int hex1 = 0x1A;
    int hex2 = 0xff;
    int hex3 = 0xDEAD;
    
    // Literali hexazecimali (uppercase X)
    int hex4 = 0XFF;
    int hex5 = 0XBEEF;
    
    // Literali binari (lowercase b)
    int bin1 = 0b101;
    int bin2 = 0b11111111;
    
    // Literali binari (uppercase B)
    int bin3 = 0B1010;
    int bin4 = 0B0;
    
    return 0;
}
